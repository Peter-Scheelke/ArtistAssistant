{
    /// <summary>
    /// The types of the images that a <see cref="DrawableObject"/> can contain
    /// </summary>
    public enum ImageType
    {
        /// <summary>
        /// A fluffy cloud
        /// </summary>
        Cloud,

        /// <summary>
        /// Some mountains
        /// </summary>
        Mountain,

        /// <summary>
        /// A rain cloud
        /// </summary>
        Rain,

        /// <summary>
        /// A tree
        /// </summary>
        Tree,

        /// <summary>
        /// A pine tree
        /// </summary>
        Pine,

        /// <summary>
        /// A pond
        /// </summary>
        Pond
    }
}